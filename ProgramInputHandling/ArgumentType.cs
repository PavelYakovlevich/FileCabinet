namespace ProgramInputHandling
{
    /// <summary>
    ///     Enum for representing a type of the paramters value.
    /// </summary>
    public enum ArgumentType
    {
        /// <summary>
        ///     Parameter's value is an integer.
        /// </summary>
        Integer,
        /// <summary>
        ///     Parameter's value is a file path.
        /// </summary>
        FilePath,
        /// <summary>
        ///     Parameter's value is an positive integer.
        /// </summary>
        PositiveInteger
    }
}